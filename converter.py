def convert_temperature():
    """–ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—ã –º–µ–∂–¥—É –¶–µ–ª—å—Å–∏–µ–º –∏ –§–∞—Ä–µ–Ω–≥–µ–π—Ç–æ–º"""
