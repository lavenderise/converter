def convert_currency():
    """–ö–æ–Ω–≤–µ—Ä—Ç–∞—Ü–∏—è –≤–∞–ª—é—Ç—ã (–º–æ–∂–Ω–æ –º–µ–Ω—è—Ç—å –∫—É—Ä—Å)"""
