    print("1. –¶–µ–ª—å—Å–∏–π ‚Üí –§–∞—Ä–µ–Ω–≥–µ–π—Ç")
    print("2. –§–∞—Ä–µ–Ω–≥–µ–π—Ç ‚Üí –¶–µ–ª—å—Å–∏–π")
    
    temp_choice = input("–í—ã–±–µ—Ä–∏—Ç–µ –Ω–∞–ø—Ä–∞–≤–ª–µ–Ω–∏–µ (1-2): ")
    
    if temp_choice == '1':
        celsius = float(input("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—É –≤ –¶–µ–ª—å—Å–∏—è—Ö: "))
        fahrenheit = (celsius * 9/5) + 32
        print(f"üìä {celsius}¬∞C = {fahrenheit:.2f}¬∞F")
    elif temp_choice == '2':
        fahrenheit = float(input("–í–≤–µ–¥–∏—Ç–µ —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä—É –≤ –§–∞—Ä–µ–Ω–≥–µ–π—Ç–∞—Ö: "))
        celsius = (fahrenheit - 32) * 5/9
        print(f"üìä {fahrenheit}¬∞F = {celsius:.2f}¬∞C")
    else:
        print("‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π –≤—ã–±–æ—Ä")

